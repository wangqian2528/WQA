#include "bsp.h"

/*
*********************************************************************************************************
 * @brief       bsp_Init
 * @note        初始化所有的硬件设备
 * @param[in]   none
 * @return      none
*********************************************************************************************************
*/
void bsp_Init(void)
{
    bsp_InitDWT();  /* 初始化DWT时钟周期计数器 */
    bsp_InitTouch();
}

/*
*********************************************************************************************************
 * @brief       bsp_RunPer1ms
 * @note        每隔1ms调用
 * @param[in]   none
 * @return      none
*********************************************************************************************************
*/
void bsp_RunPer1ms(void)
{
    bsp_TouchScan(0);
}
